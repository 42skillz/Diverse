namespace Diverse
{
    /// <summary>
    /// Gender of a <see cref="Person"/>.
    /// </summary>
    public enum Gender
    {
        /// <summary>
        /// Male.
        /// </summary>
        Male,
        /// <summary>
        /// Female.
        /// </summary>
        Female,
        /// <summary>
        /// Non binary.
        /// </summary>
        NonBinary
    }

    /// <summary>
    /// Gender title.
    /// </summary>
    public enum GenderTitle
    {
        /// <summary>
        /// Mister.
        /// </summary>
        Mr,
        /// <summary>
        /// Missus.
        /// </summary>
        Mrs,
        /// <summary>
        /// Addressing a grown woman as "Ms." is safer than "Miss" or "Mrs."
        /// </summary>
        Ms,
        /// <summary>
        /// Miks or Muks (used for Non-binary people).
        /// </summary>
        Mx
    }
}

